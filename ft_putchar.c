#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

void	ft_putstrn(char *str, int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		ft_putchar(str[i]);
		i++;
	}
}
